# Nesting Structure Comparison

"""
Complete the function/method (depending on the language) to return true/True when its argument is an array that has the same nesting structures and same corresponding length of nested arrays as the first array.

For example:

# should return True
same_structure_as([ 1, 1, 1 ], [ 2, 2, 2 ] )
same_structure_as([ 1, [ 1, 1 ] ], [ 2, [ 2, 2 ] ] )

# should return False 
same_structure_as([ 1, [ 1, 1 ] ], [ [ 2, 2 ], 2 ] )
same_structure_as([ 1, [ 1, 1 ] ], [ [ 2 ], 2 ] )

# should return True
same_structure_as([ [ [ ], [ ] ] ], [ [ [ ], [ ] ] ] )

# should return False
same_structure_as([ [ [ ], [ ] ] ], [ [ 1, 1 ] ] )
"""

def same_structure_as(original,other):
    if type(original) != list or type(other) != list or len(original) != len(other) or type(original) != type(other):
        return False
    for ori, oth in zip(original, other):
        if type(ori) != type(oth) and ori not in other:
            return False
        if type(ori) is list and type(oth) is list:
            return same_structure_as(ori, oth)
    return True

